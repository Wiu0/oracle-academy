package br.com.oracle.lesson2.classe.objeto;

/**
 * Classe de Objeto. Responsabilidade da classe é apenas possuir as caracteristicas e 
 * metodos necessário para representar o processo de "soma"
 *@author wiu
 *@since 20181206
 * */
public class Soma {

	/**
	 * Metodo para calcular a soma de dois numeros
	 * @param n1. Valor do primeiro numero a ser somado
	 * @param n2. Valor do segundo numero a ser somado
	 * @return soma entre n1 + n2
	 */
	public double calcular(double n1, double n2) {
		return n1 + n2;
	}
}
