package br.com.oracle.lesson2.gas;

/**
 * Pacote gas: g = getter a = and s = setter
 * Metodos getter e setter permitem que criemos um encapsulamento sobre os atributos do objeto
 * @author wiu
 *
 */
public class GetterSetter {

	private int idade;

	public int getIdade() {
		return idade;
	}

	public void setIdade(int idade) {
		this.idade = idade;
	}

}
